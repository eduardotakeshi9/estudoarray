const exemploArray = [1, 2, 3];

/* Array.indexOf() - Retorna o primeiro indice em que o elemento pode ser encontrado no array. Caso o elemento não exista, retorna -1. 
   1º parametro - Elemento a ser encontrado no array.
   2º parametro - Índice no qual vai começar a pesquisa. (Opcional)
*/

const arrayIndexOf = exemploArray.indexOf(2);
console.log(arrayIndexOf);
// Retorna 1, porque o elemento 2 se encontra na posição 1 do array.