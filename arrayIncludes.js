const exemploArray = [1, 2, 3];

/* Array.includes() - Determina se um array inclui um determinado elemento, retornado true ou false 
   1º parametro - Valor a ser pesquisado.
   2º parametro - Índice no qual vai começar a pesquisa. (Opcional)
*/

const arrayIncludes = exemploArray.includes(2);
console.log(arrayIncludes);
// Retorna true, porque o elemento 2 se encontra no exemploArray.