/* Array.isArray() - Determinado se o valor passado é um array. Retornando um booleano, true se é array e false se não é.
  1º parametro - O valor a ser verificado
*/

console.log(Array.isArray([1, 3, 5]));
// Retorna true, pois é um array.

console.log(Array.isArray(2));
// Retorna false, pois não é um array.
